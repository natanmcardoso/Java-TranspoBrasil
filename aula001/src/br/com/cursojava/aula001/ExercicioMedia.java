package br.com.cursojava.aula001;

public class ExercicioMedia {
	public static void main(String[] args){
		int nota1= 10;
		int nota2= 9;
		int nota3= 8;
		
		System.out.printf("%d + %d + %d = %d \n", nota1, nota2, nota3, nota1+ nota2+ nota3);
		System.out.printf("%d + %d + %d / 3 = %d \n", nota1, nota2, nota3, (nota1+ nota2+ nota3) / 3);
		
	}

}